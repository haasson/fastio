// Валидация ID систем аналитики.
//
// ID интерполируются в inline-script на витрине (gtag, ym), поэтому формат
// должен быть строго проверен и в админке (на сохранении), и на витрине
// (на чтении из tenant.seo). Если формат разойдётся между сторонами —
// manager может сохранить «валидное» значение, которое витрина молча
// выбросит. Поэтому единый источник истины здесь.

export const GA_ID_RE = /^G-[A-Z0-9]{4,15}$/i
export const YM_ID_RE = /^\d{5,12}$/
