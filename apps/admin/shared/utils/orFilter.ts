// PostgREST `.or(...)` принимает фильтр строкой, где запятые разделяют условия,
// а скобки группируют. Пользовательский ввод, попавший в значение `ilike`, может
// содержать `, ( ) "` и сломать разбор фильтра (400) или изменить его логику.
// Глушим структурные символы пробелом — для подстрочного `ilike`-поиска это
// безвредно (символы и так не индексируются осмысленно).
export const sanitizeOrSearch = (value: string): string => value.replace(/[,()"]/g, ' ').trim()
