-- 335: цена опции модификатора (dish_modifier_options.price_delta) не может быть отрицательной.
--
-- Найдено в ручном тестинге (2026-06-12): на витрине ценник «от X ₽» показывал
-- базовую цену, хотя опция с отрицательной price_delta (напр. Капучино, размер
-- «Маленький» = -30) делала реальный минимум ниже. Решение юзера — не чинить
-- отображение, а ЗАПРЕТИТЬ отрицательную цену модификатора вовсе: дельта опции
-- всегда ≥ 0 (база = самая дешёвая конфигурация, опции только добавляют).
--
-- UI-запрет (UiInputNumber блокирует ввод '-' при min ≥ 0) — не страхует от
-- прямого SQL/API, поэтому ставим жёсткий CHECK на уровне БД.

-- 1. Почистить уже лежащие отрицательные дельты (тестовые данные T1: Капучино/Эспрессо).
UPDATE dish_modifier_options
SET price_delta = 0
WHERE price_delta < 0;

-- 2. Жёсткий гард: новые/изменённые дельты не могут быть < 0 (прямой SQL/API тоже).
ALTER TABLE dish_modifier_options
  ADD CONSTRAINT dish_modifier_options_price_delta_nonneg
  CHECK (price_delta >= 0);
